package java.lang;
public class IllegalArgumentException
  extends java.lang.RuntimeException
{
public  IllegalArgumentException() { throw new RuntimeException("Stub!"); }
public  IllegalArgumentException(java.lang.String arg0) { throw new RuntimeException("Stub!"); }
public  IllegalArgumentException(java.lang.String arg0, java.lang.Throwable arg1) { throw new RuntimeException("Stub!"); }
public  IllegalArgumentException(java.lang.Throwable arg0) { throw new RuntimeException("Stub!"); }
}
