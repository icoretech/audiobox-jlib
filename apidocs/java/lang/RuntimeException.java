package java.lang;
public class RuntimeException
  extends java.lang.Exception
{
public  RuntimeException() { throw new RuntimeException("Stub!"); }
public  RuntimeException(java.lang.String arg0) { throw new RuntimeException("Stub!"); }
public  RuntimeException(java.lang.String arg0, java.lang.Throwable arg1) { throw new RuntimeException("Stub!"); }
public  RuntimeException(java.lang.Throwable arg0) { throw new RuntimeException("Stub!"); }
protected  RuntimeException(java.lang.String arg0, java.lang.Throwable arg1, boolean arg2, boolean arg3) { throw new RuntimeException("Stub!"); }
}
