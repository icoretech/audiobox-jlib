package java.lang;
public class IllegalStateException
  extends java.lang.RuntimeException
{
public  IllegalStateException() { throw new RuntimeException("Stub!"); }
public  IllegalStateException(java.lang.String arg0) { throw new RuntimeException("Stub!"); }
public  IllegalStateException(java.lang.String arg0, java.lang.Throwable arg1) { throw new RuntimeException("Stub!"); }
public  IllegalStateException(java.lang.Throwable arg0) { throw new RuntimeException("Stub!"); }
}
